const DEFAULT_LANG = 'en';

// Cached language — update via setLang() to avoid repeated localStorage reads
let _lang = DEFAULT_LANG;

if (typeof localStorage !== 'undefined') {
    const savedLang = localStorage.getItem('lang');
    if (TRANSLATIONS[savedLang]) {
        _lang = savedLang;
    }
}

function getLang() {
    return _lang;
}

function setLang(code) {
    _lang = TRANSLATIONS[code] ? code : DEFAULT_LANG;
    if (typeof localStorage !== 'undefined') {
        localStorage.setItem('lang', _lang);
    }
}

function t(key, vars = {}) {
    const dict = TRANSLATIONS[_lang] || TRANSLATIONS[DEFAULT_LANG];
    let str = dict[key] || TRANSLATIONS[DEFAULT_LANG][key] || key;
    Object.entries(vars).forEach(([k, v]) => {
        str = str.replace(`{${k}}`, v);
    });
    return str;
}

function tArr(key) {
    const dict = TRANSLATIONS[_lang] || TRANSLATIONS[DEFAULT_LANG];
    return dict[key] || TRANSLATIONS[DEFAULT_LANG][key] || [];
}

function getLanguageName(code) {
    return TRANSLATIONS[code]?.langName || code;
}

function getSupportedLangs() {
    return Object.keys(TRANSLATIONS);
}
